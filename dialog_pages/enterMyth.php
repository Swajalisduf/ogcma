	<p>Reid’s OGCMA catalogues modern usages by their classical myth. We short-hand the categories by the headings in Reid, e.g. “Eos” or “EosTithonus” depending on whether the usage is catalogued s.v. “Eos — General List” or “Eos and Tithonus”. Please check in Reid’s OGCMA index for guidance or, in most cases, the pull-down menu will provide direction. Most “Myth” entries in this field will be a) proper names of individual mythological figures — Cassandra, Hector, Paris, Polydorus — and b) reduced grammatically, e.g. “HeraclesNemeanLion” for Reid’s “Heracles, Labors of — The Nemean Lion”, and c) written without conjunctions or spaces, as “IcarusDaedalus” for Reid’s “Icarus and Daedalus”.</p> 
